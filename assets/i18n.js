  getLang() {
    return localStorage.getItem('app_lang') || 'ar';
  },

  setLang(lang) {
    localStorage.setItem('app_lang', lang);
  },

  _tempLang: null,

  applyTemp(lang) {
    const prev = this.getLang();
    this._tempLang = lang || null;
    try {
      if (lang) {
        document.documentElement.lang = lang;
        document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
      } else {
        document.documentElement.lang = prev;
        document.documentElement.dir = prev === 'ar' ? 'rtl' : 'ltr';
      }

      document.querySelectorAll('[data-i18n]').forEach(el => {
        el.textContent = (this._translations[lang] && this._translations[lang][el.dataset.i18n]) || el.dataset.i18n;
      });

      document.querySelectorAll('[data-i18n-html]').forEach(el => {
        const k = el.dataset.i18nHtml;
        el.innerHTML = (this._translations[lang] && this._translations[lang][k]) || k;
      });

      document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const k = el.dataset.i18nPlaceholder;
        el.placeholder = (this._translations[lang] && this._translations[lang][k]) || k;
      });

      document.querySelectorAll('[data-i18n-title]').forEach(el => {
        const k = el.dataset.i18nTitle;
        el.title = (this._translations[lang] && this._translations[lang][k]) || k;
      });

      document.querySelectorAll('[data-i18n-alt]').forEach(el => {
        const k = el.dataset.i18nAlt;
        el.alt = (this._translations[lang] && this._translations[lang][k]) || k;
      });

      document.querySelectorAll('.field-input').forEach(input => {
        input.style.direction = lang === 'ar' ? 'rtl' : 'ltr';
        input.style.textAlign = lang === 'ar' ? 'right' : 'left';
      });
    } finally {
      document.documentElement.dataset.prevLang = prev;
    }
  },

  enableArabicPrint() {
    const KEY = '__i18n_print_ar_enabled__';
    if (window[KEY]) return;
    window[KEY] = true;

    let prevLang = null;
    const before = () => {
      prevLang = this.getLang();
      this.applyTemp('ar');
      window.dispatchEvent(new CustomEvent('print-translate', { detail: { lang: 'ar', prevLang } }));
    };
    const after = () => {
      this.applyTemp(prevLang || this.getLang());
      window.dispatchEvent(new CustomEvent('print-restore', { detail: { lang: prevLang || this.getLang() } }));
      prevLang = null;
    };

    window.addEventListener('beforeprint', before);
    window.addEventListener('afterprint', after);
  },

  t(key) {
    const lang = this._tempLang || this.getLang();
    return (this._translations[lang] && this._translations[lang][key]) || key;
  },

  apply(lang) {
    if (lang) this.setLang(lang);
    const isAr = this.getLang() === 'ar';

    document.documentElement.lang = this.getLang();
    document.documentElement.dir = isAr ? 'rtl' : 'ltr';

    document.querySelectorAll('[data-i18n]').forEach(el => {
      el.textContent = this.t(el.dataset.i18n);
    });

    document.querySelectorAll('[data-i18n-html]').forEach(el => {
      el.innerHTML = this.t(el.dataset.i18nHtml);
    });

    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      el.placeholder = this.t(el.dataset.i18nPlaceholder);
    });

    document.querySelectorAll('[data-i18n-title]').forEach(el => {
      el.title = this.t(el.dataset.i18nTitle);
    });

    document.querySelectorAll('[data-i18n-alt]').forEach(el => {
      el.alt = this.t(el.dataset.i18nAlt);
    });

    const pageTitleKey = document.body.dataset.pageTitle;
    if (pageTitleKey) document.title = this.t(pageTitleKey);

    document.querySelectorAll('.field-input').forEach(input => {
      input.style.direction = isAr ? 'rtl' : 'ltr';
      input.style.textAlign = isAr ? 'right' : 'left';
    });
  }
